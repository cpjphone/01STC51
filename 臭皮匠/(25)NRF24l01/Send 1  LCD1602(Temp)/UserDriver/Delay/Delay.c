/********************************************************************************************
函数名称：	void Delay(uint m)
描述	：	长延时
输入	：	无
输出	：	无
返回	：	无
注意事项：	无
*********************************************************************************************/
void Delay(unsigned int m)
{
	unsigned int i;
	for(i=0; i<m; i++);
		for(i=0; i<m; i++);
}

/*****************************************************************************
函数名称：	void inerDelay_us(uchar m)
描述	：
输入	：	无
输出	：	无
返回	：	无
注意事项：	无
******************************************************************************/
void inerDelay_us(uchar m)
{
	for(;m>0;m--)
		_nop_();
}

/********************************************************************************************
函数名称：void Delayms(uint m)
函数功能：延时程序，大约1ms
入口参数：m
出口参数：无
*********************************************************************************************/
void Delayms(uint m)
{
	uint i,j;
	for(i = 0;i < m;i++)
		for(j = 0;j < 121;j++);			
}
